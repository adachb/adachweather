public class Condition{
    private int code;
    private int temperature;
    private String text;
    private String date;
    private String day;
    private int high;
    private int low;


    public int getCode() {
        return code;
    }

    public int getTemperature() {
        return temperature;
    }

    public String getText() {
        return text;
    }

    public String getDate() {
        return date;
    }

    public String getDay() {
        return day;
    }

    public int getHigh() {
        return high;
    }

    public int getLow() {
        return low;
    }

}
